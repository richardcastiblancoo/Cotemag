<script>
    // Script simplificado para manejar modales
    document.addEventListener('DOMContentLoaded', function() {
        // Agregar evento de clic al fondo de los modales
        document.querySelectorAll('.ctmg-modal').forEach(modal => {
            modal.addEventListener('click', function(e) {
                if (e.target === this) cerrarModal(this.id);
            });
        });
    });

    // Función para abrir modal
    function abrirModal(id) {
        const modal = document.getElementById(id);
        modal.style.display = 'block';
        setTimeout(() => modal.classList.add('ctmg-mostrar'), 10);
    }

    // Función para cerrar modal
    function cerrarModal(id) {
        const modal = typeof id === 'string' ? document.getElementById(id) : id;
        modal.classList.remove('ctmg-mostrar');
        setTimeout(() => modal.style.display = 'none', 300);
    }
</script>